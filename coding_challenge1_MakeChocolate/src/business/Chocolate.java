package business;

public abstract class Chocolate {
	// instance variables
	protected int barCount;

	public Chocolate(int barCount) {
		super();
		this.barCount = barCount;
	}

	public int getBarCount() {
		return barCount;
	}

	public void setBarCount(int barCount) {
		this.barCount = barCount;
	}
	
	
	
	
}
